def warn(tag, text):
	print("[WARN][" + tag + "]: " + text)

def ok(tag, text):
	print("[OK][" + tag + "]: " + text)

def debug(tag, text):
	print("[DEBUG][" + tag + "]: " + text)

def error(tag, text):
	print("[ERROR][" + tag + "]: " + text)

def info(tag, text):
	print("[INFO][" + tag + "]: " + text)