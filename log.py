def warn(tag, text):
	print("[WARN][" + str(tag) + "]: " + str(text))

def ok(tag, text):
	print("[OK][" + str(tag) + "]: " + str(text))

def debug(tag, text):
	print("[DEBUG][" + str(tag) + "]: " + str(text))

def error(tag, text):
	print("[ERROR][" + str(tag) + "]: " + str(text))

def info(tag, text):
	print("[INFO][" + str(tag) + "]: " + str(text))